#7 - Escreva um programa que escreva na tela, de 1 até 100, de 1 em 1, 2 vezes. A primeira vez deve
#usar a estrutura de repetição for() e a segunda while().

## Usando o for

for i in range(1, 101):
    print('Usando o for: ', i)

# Usando o While

num = 1

while num <= 100:
    print('Usando o while: ', num)
    num += 1